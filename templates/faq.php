<script>
document.addEventListener('DOMContentLoaded', function() {
    // Gestion des questions/réponses
    const faqItems = document.querySelectorAll('.faq-item');
    
    faqItems.forEach(item => {
        const question = item.querySelector('.faq-question');
        
        question.addEventListener('click', () => {
            // Fermer toutes les autres réponses
            faqItems.forEach(otherItem => {
                if (otherItem !== item && otherItem.classList.contains('active')) {
                    otherItem.classList.remove('active');
                }
            });
            
            // Basculer l'état actif de l'élément cliqué
            item.classList.toggle('active');
        });
    });
    
    // Recherche dans la FAQ
    const searchInput = document.getElementById('faq-search');
    
    searchInput.addEventListener('input', function() {
        const searchTerm = this.value.toLowerCase();
        
        faqItems.forEach(item => {
            const question = item.querySelector('.faq-question h3').textContent.toLowerCase();
            const answer = item.querySelector('.faq-answer').textContent.toLowerCase();
            
            if (question.includes(searchTerm) || answer.includes(searchTerm)) {
                item.style.display = 'block';
                if (searchTerm.length > 2) {
                    item.classList.add('active');
                }
            } else {
                item.style.display = 'none';
            }
        });
        
        // Afficher/masquer les catégories en fonction des résultats
        document.querySelectorAll('.faq-category').forEach(category => {
            const visibleItems = category.querySelectorAll('.faq-item[style="display: block"]').length;
            category.style.display = visibleItems > 0 ? 'block' : 'none';
        });
    });
});
</script>